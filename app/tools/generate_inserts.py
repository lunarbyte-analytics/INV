def _split_rows(raw: str):
    lines = [ln for ln in raw.splitlines() if ln.strip()]
    header = lines[0].split("\t")
    rows = []
    for ln in lines[1:]:
        parts = ln.split("\t")
        # Dopasuj liczbę kolumn – w danych niektóre pola bywają puste
        while len(parts) < len(header):
            parts.append("")
        rows.append(dict(zip(header, parts)))
    return header, rows

def _none_if(v: str):
    v = v.strip()
    if v == "" or v.upper() == "NULL":
        return None
    return v

def _int(v, default=None):
    v = _none_if(str(v))
    return int(v) if v is not None and v != "" else default

def _flt(v, default=None):
    v = _none_if(str(v))
    if v is None or v == "":
        return default
    return float(v.replace(",", "."))

def _q(v):
    """SQL literal (NULL / 'text') – bez ucieczki znaków specjalnych poza apostrofem."""
    if v is None:
        return "NULL"
    v = str(v)
    v = v.replace("'", "''")
    return f"'{v}'"

def parse_input():
    inv_header, inv_rows = _split_rows(INVOICE_RAW)
    det_header, det_rows = _split_rows(DETAIL_RAW)

    invoices = []
    for r in inv_rows:
        invoices.append({
            "old_id": _int(r["InvoiceId"]),
            "CompanyId": _int(r["CompanyId"]),
            "CustomerId": _int(r["CustomerId"]),
            "PaymentMethodId": _int(r["PaymentMethodId"]),
            "StatusId": _int(r["StatusId"]),
            "TypeId": _int(r["TypeId"]),
            "IsAdditionalAddress": 1 if str(r["IsAdditionalAddress"]).strip() in ("1","true","True","YES","yes") else 0,
            "Name": _none_if(r["Name"]),
            "CreateDate": _none_if(r["CreateDate"]),
            "SalesDate": _none_if(r["SalesDate"]),
            "PaymentDate": _none_if(r["PaymentDate"]),
            "Created": _none_if(r["Created"]),
            "Updated": _none_if(r["Updated"]),
        })

    details = []
    for r in det_rows:
        details.append({
            "old_detail_id": _int(r["InvoiceDetailId"]),
            "old_invoice_id": _int(r["InvoiceId"]),
            "ServiceId": _int(r["ServiceId"]),
            "Quantity": _flt(r["Quantity"], 0.0),
            "Created": _none_if(r["Created"]),
            "Updated": _none_if(r["Updated"]),
        })
    return invoices, details

def normalize_order(invoices):
    def _dt(s):
        if not s:
            return datetime.max
        # obsłuż YYYY-MM-DD HH:MM:SS.x lub YYYY-MM-DD
        try:
            return datetime.fromisoformat(s.replace("Z",""))
        except Exception:
            try:
                return datetime.strptime(s, "%Y-%m-%d")
            except Exception:
                return datetime.max
    # stabilny porządek: CreateDate, SalesDate, old_id
    invoices.sort(key=lambda r: (_dt(r["CreateDate"]), _dt(r["SalesDate"]), r["old_id"]))
    return invoices

def renumber(invoices, details):
    # 1) mapuj InvoiceId
    normalize_order(invoices)
    old_to_new = {}
    for i, r in enumerate(invoices, start=1):
        old_to_new[r["old_id"]] = i
        r["new_id"] = i

    # 2) przemapuj detale + nadaj nowe ID
    # sortowanie detali po starym InvoiceId, potem po starym detail id
    details.sort(key=lambda d: (d["old_invoice_id"], d["old_detail_id"]))
    for j, d in enumerate(details, start=1):
        d["new_detail_id"] = j
        d["new_invoice_id"] = old_to_new[d["old_invoice_id"]]

    return old_to_new

def build_sql(invoices, details):
    out = []
    out.append("-- Wygenerowano automatycznie: renumeracja InvoiceId i InvoiceDetailId na 1..N")
    out.append("PRAGMA foreign_keys=OFF;")
    out.append("")
    out.append("-- (opcjonalnie) wyczyść tabele, jeśli chcesz czysty insert:")
    out.append("-- DELETE FROM InvoiceDetail;")
    out.append("-- DELETE FROM Invoice;")
    out.append("")

    # INSERTY Invoice
    out.append("-- ===== Invoice =====")
    out.append("INSERT INTO Invoice (InvoiceId, CompanyId, CustomerId, PaymentMethodId, StatusId, TypeId, IsAdditionalAddress, Name, CreateDate, SalesDate, PaymentDate, Created, Updated) VALUES")
    inv_values = []
    for r in invoices:
        inv_values.append(
            f"({r['new_id']}, {r['CompanyId']}, {r['CustomerId']}, {r['PaymentMethodId']}, {r['StatusId']}, {r['TypeId']}, {r['IsAdditionalAddress']}, "
            f"{_q(r['Name'])}, {_q(r['CreateDate'])}, {_q(r['SalesDate'])}, {_q(r['PaymentDate'])}, {_q(r['Created'])}, {_q(r['Updated'])})"
        )
    out.append(",\n".join(inv_values) + ";")
    out.append("")

    # INSERTY InvoiceDetail
    out.append("-- ===== InvoiceDetail =====")
    out.append("INSERT INTO InvoiceDetail (InvoiceDetailId, InvoiceId, ServiceId, Quantity, Created, Updated) VALUES")
    det_values = []
    for d in details:
        det_values.append(
            f"({d['new_detail_id']}, {d['new_invoice_id']}, {d['ServiceId']}, {d['Quantity']:.2f}, {_q(d['Created'])}, {_q(d['Updated'])})"
        )
    out.append(",\n".join(det_values) + ";")
    out.append("")
    out.append("PRAGMA foreign_keys=ON;")
    out.append("")
    return "\n".join(out)

def main():
    # 1) wczytaj
    invoices, details = parse_input()

    # 2) renumeruj
    renumber(invoices, details)

    # 3) zbuduj SQL
    sql = build_sql(invoices, details)

    # 4) zapisz plik i wydrukuj na stdout
    OUT_FILE.write_text(sql, encoding="utf-8")
    print(sql)

if __name__ == "__main__":
    main()
